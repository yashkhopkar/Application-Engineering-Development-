    private void btnDeleteAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAccountActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAccounts.getSelectedRow();
        if(selectedRow>=0)
        {
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the account details?","Warning",dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION)
                {
                    Account account = (Account)tblAccounts.getValueAt(selectedRow, 0);
                    accountdir.deleteAccount(account);
                    populateTable();
                }
        }
        else
        {
        JOptionPane.showMessageDialog(null,"Please select a row from the table", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteAccountActionPerformed

    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAccounts.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null,"Please select a row from the table", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        Account account = (Account)tblAccounts.getValueAt(selectedRow, 0);
        ViewAccountJPanel panel = new ViewAccountJPanel(userProcessContainer, account);
        userProcessContainer.add("ViewAccountJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(txtSearchAccountNumber.getText().equals(""))
                {
                    JOptionPane.showMessageDialog(null, "Please Enter a number");
                }
        else{
        Account result = accountdir.searchAccount(txtSearchAccountNumber.getText());
        if(result == null)
        JOptionPane.showMessageDialog(null,"Account number you entered does not exist", "Information", JOptionPane.INFORMATION_MESSAGE);
        else
        {
            ViewAccountJPanel panel = new ViewAccountJPanel(userProcessContainer, result);
        userProcessContainer.add("ViewAccountJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void txtSearchAccountNumberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchAccountNumberKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();
        if((key>=evt.VK_0&&key<=evt.VK_9)||(key>=evt.VK_NUMPAD0&&key<=evt.VK_NUMPAD9)||key==evt.VK_BACK_SPACE)
        {
        txtSearchAccountNumber.setEditable(true);
        }
        else
        {
            txtSearchAccountNumber.setEditable(false);
        }
    }//GEN-LAST:event_txtSearchAccountNumberKeyPressed


