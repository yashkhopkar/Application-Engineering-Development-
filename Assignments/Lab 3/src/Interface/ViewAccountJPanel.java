    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if((txtAccountNumber.getText().equals(""))||(txtRoutingNumber.getText().equals(""))||(txtBankName.getText().equals("")))
        {
        JOptionPane.showMessageDialog(null, "All fields are mandatory");
        }
        else
        {
        account.setRoutingNumber(txtRoutingNumber.getText());
        account.setAccountNumber(txtAccountNumber.getText());
        account.setBankName(txtBankName.getText());
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        
        JOptionPane.showMessageDialog(null, "Account Updated Successfully");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void txtRoutingNumberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRoutingNumberKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();
        if((key>=evt.VK_0&&key<=evt.VK_9)||(key>=evt.VK_NUMPAD0&&key<=evt.VK_NUMPAD9)||key==evt.VK_BACK_SPACE)
        {
        txtRoutingNumber.setEditable(true);
        }
        else
        {
            txtRoutingNumber.setEditable(false);
        }
    }//GEN-LAST:event_txtRoutingNumberKeyPressed

