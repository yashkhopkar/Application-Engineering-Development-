    private void btnUpdateUserAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateUserAccountActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManageUserAccountDir.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null, "Please select a row first");
        }
        else
        {
            UserAccount user =(UserAccount)tblManageUserAccountDir.getValueAt(selectedRow, 0);
        UpdateUserAccountJPanel panel = new UpdateUserAccountJPanel(CardSequenceJPanel,userAccountList,user);
        CardSequenceJPanel.add("UpdateUserAccountJPanel",panel);
        CardLayout layout = (CardLayout) CardSequenceJPanel.getLayout();
        layout.next(CardSequenceJPanel);
        }
    }//GEN-LAST:event_btnUpdateUserAccountActionPerformed

    private void btnDeleteAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAccountActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManageUserAccountDir.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null, "Please select a row first");
        }
        else
        {
        UserAccount user = (UserAccount)tblManageUserAccountDir.getValueAt(selectedRow, 0);
        userAccountList.deleteUserAccount(user);
        populateUserAccountDirectory();
        }
    }//GEN-LAST:event_btnDeleteAccountActionPerformed


