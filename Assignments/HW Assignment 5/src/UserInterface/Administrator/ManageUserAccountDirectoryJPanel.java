    public void populateUserAccountDirectory()
    {
        DefaultTableModel dtm = (DefaultTableModel)tblManageUserAccountDir.getModel();
        dtm.setRowCount(0);
        for(UserAccount u :userAccountList.getUserAccountList())
        {
        Object [] row = new Object[3];
        row[0] = u;
        row[1] = u.getPassword();
        row[2] = u.getRole();
        dtm.addRow(row);
        }
        
    }
