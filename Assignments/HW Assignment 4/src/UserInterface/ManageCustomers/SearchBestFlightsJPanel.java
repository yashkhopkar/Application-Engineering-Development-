    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:

        if(txtSourceAirport.getText().equals("")||txtDestinationAirport.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "All feilds are manadatory");
        }
        else
        {
            String srcApt = txtSourceAirport.getText();
            String dstApt = txtDestinationAirport.getText();
            boolean record = false;
            DefaultTableModel dtm = (DefaultTableModel)tblMngTravelAgency.getModel();
            dtm.setRowCount(0);
            for(Airliner a : aircat.getAirlinerList())
            {
                for(Flight f:a.getFlightList())
                {
                    if((f.getSourceAirport().equalsIgnoreCase(srcApt))&& f.getDestinationAirport().equalsIgnoreCase(dstApt))
                    {
                        Object [] row = new Object[7];
                        row[0] = f;
                        row[1] = f.getFlightNo();
                        row[2] = f.getDate();
                        row[3] = f.getTime();
                        row[4] = f.getNumberOfSeats();
                        row[5] = f.getSourceAirport();
                        row[6] = f.getDestinationAirport();
                        dtm.addRow(row);
                        record = true;
                    }
                }
            }
            if(!record)
            {
                JOptionPane.showMessageDialog(null, "Sorry, No suitable flights found");
            }

        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnBookFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookFlightActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMngTravelAgency.getSelectedRow();
        if(selectedRow>=0)
        {
        Flight f= (Flight)tblMngTravelAgency.getValueAt(selectedRow, 0);    
        BookFlightJPanel panel = new BookFlightJPanel(CardSequenceJPanel,f);
         
        CardSequenceJPanel.add("BookFlightJPanel",panel);
        CardLayout layout = (CardLayout) CardSequenceJPanel.getLayout();
        layout.next(CardSequenceJPanel);
        }
        else
            JOptionPane.showMessageDialog(null, "Please select a flight first");
    }//GEN-LAST:event_btnBookFlightActionPerformed


