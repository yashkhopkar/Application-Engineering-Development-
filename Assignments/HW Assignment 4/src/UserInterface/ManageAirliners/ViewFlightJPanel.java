     public void populateTable1()
    {
        DefaultTableModel dtm  =(DefaultTableModel) tblViewFlight.getModel();
        dtm.setRowCount(0);
        for(Flight f :air.getFlightList())
        {
            Object [] row = new Object[7];
            row[0] = f;
            row[1] = f.getFlightNo();
            row[2] = f.getDate();
            row[3] = f.getTime();
            row[4] = f.getNumberOfSeats();
            row[5] = f.getSourceAirport();
            row[6] = f.getDestinationAirport();
            dtm.addRow(row);
        }
    }

