    private void btnUpdateFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateFlightActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblViewFlight.getSelectedRow();
        if(selectedRow>=0)
        {
        Flight f = (Flight)tblViewFlight.getValueAt(selectedRow, 0);
        txtFlightNo.setText(f.getFlightNo());
        txtSerialNo.setText(String.valueOf(f.getSerialNo()));
        txtDate.setText(f.getDate());
        txtTime.setText(f.getTime());
        txtNumberOfSeats.setText(String.valueOf(f.getNumberOfSeats()));
        txtSrouceAirport.setText(f.getSourceAirport());
        txtDestinationAirport.setText(f.getDestinationAirport());
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a row first");
        }
    }//GEN-LAST:event_btnUpdateFlightActionPerformed

    private void btnSaveFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveFlightActionPerformed
        // TODO add your handling code here:
         if((txtSerialNo.getText().equals(""))||(txtFlightNo.getText().equals(""))||(txtDate.getText().equals(""))||(txtTime.getText().equals(""))||(txtNumberOfSeats.getText().equals(""))||(txtSrouceAirport.getText().equals(""))||(txtDestinationAirport.getText().equals("")))
        {
            JOptionPane.showMessageDialog(null, "All feilds are mandatory");
        }
        else
         {
             int srNo = Integer.parseInt(txtSerialNo.getText());
             String flightNo = txtFlightNo.getText();
             String date = txtDate.getText();
             String time = txtTime.getText();
             int noOfSeats = Integer.parseInt(txtDate.getText());
             String srcApt = txtSrouceAirport.getText();
             String dstApt = txtDestinationAirport.getText();
             Flight f =air.addFlight();
             f.setSerialNo(srNo);
             f.setFlightNo(flightNo);
             f.setDate(date);
             f.setTime(time);
             f.setNumberOfSeats(noOfSeats);
             f.setSourceAirport(srcApt);
             f.setDestinationAirport(dstApt);
             populateTable1();
         
         }
    }//GEN-LAST:event_btnSaveFlightActionPerformed

