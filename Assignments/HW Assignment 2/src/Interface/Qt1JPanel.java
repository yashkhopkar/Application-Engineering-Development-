        // TODO add your handling code here:
        //Airplane plane =aircat.addAirplane();
        String  name = txtName.getText();
            boolean record=false;
            DefaultTableModel dtm = (DefaultTableModel)tblQt1.getModel();
            dtm.setRowCount(0);
            for(Airplane p :aircat.getAirplaneCatalog()) 
           {   if(p.getAirlineName().equalsIgnoreCase(name))
            {
                if(p.getAvailabe().equalsIgnoreCase("Y"))
                {
                
                Object[] row = new Object[5];
                row[0] = p.getAirlineName();
                row[1] = p.getModelNo();
                row[2] = p.getManufacturedBy();
                row[3] = p.getAirport();
                row[4] =p.getNumberOfSeats();
                dtm.addRow(row);
                record = true;
                break;
                }
            }
                  
             }
             if(!record){
             JOptionPane.showMessageDialog(null, "Record not found");
            
             
            }
       
        
        
