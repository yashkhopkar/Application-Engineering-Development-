    private void btnSearchByAirportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchByAirportActionPerformed
        // TODO add your handling code here:
        boolean record = false;
        String airport = txtAirport.getText();
        DefaultTableModel dtm = (DefaultTableModel)tblQt10.getModel();
            dtm.setRowCount(0);
            for(Airplane p:aircat.getAirplaneCatalog())
            {
                if(airport.equalsIgnoreCase(p.getAirport()))
                {
                Object[] col = new Object[6];
                col[0] = p.getAirlineName();
                col[1] = p.getModelNo();
                col[2] = p.getManufacturedBy();
                col[3] = p.getManifactureYear();
                col[4] = p.getNumberOfSeats();
                col[5] = p.getAvailabe();
                dtm.addRow(col);
                record = true;
                }
                
            }
            if(!record)
            {
            JOptionPane.showMessageDialog(null, "Record not found");
            }
    }//GEN-LAST:event_btnSearchByAirportActionPerformed


